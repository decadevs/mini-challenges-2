/* function convert morse code to english text
* @ mydata - inputed code
* @myString - string of code
* function return decoded morse code
*/

function morse(text) {
  if (text ==='') return''
  if(typeof text !== 'string') throw new Error('Please provide a morse string')

  const mydata =text.trim()
  const myString = mydata.split(/\s{3,}/)

  let myOutPut = ''

  myString.forEach((i) =>{
    i.split(' ').forEach((j) =>{
      myOutPut += MORSE_CODE[j]
    })

    myOutPut += ' '
  })


  return myOutPut.trim()
  
}

module.exports = morse;
